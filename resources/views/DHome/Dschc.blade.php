                        <h2 class="text-center">Delta State Contributory Health Commission</h2>
                        <p>The Delta State Contributory Health Commission is a Healthcare Financing organization established
                            by the
                            Delta State Government to ensure access to quality healthcare services for all residents of
                            Delta State
                            irrespective of their socio-economic status and geographical location, in an effort to achieve
                            the United
                            Nations' Sustainable Development Goal 3 in the year 2030.</p>
                        <p>The United Nations' Sustainable Development Goal 3 [Ensure Healthy Lives and Promote Well-being
                            for all
                            at all Ages] has the attainment of Universal Health Coverage (UHC) as one of its key
                            deliverables.</p>
                        <p>According to the World Health Organization, Universal Health Coverage (UHC) means that every
                            person and
                            community can have access to the promotive, preventive, curative, rehabilitative and palliative
                            health
                            services they need, of sufficient quality to be effective, while also ensuring that the use of
                            these
                            services does not expose the user to financial hardship.</p>
                        <p>During the United Nations Sustainable Development Summit in 2015, world leaders agreed to achieve
                            UHC by
                            2030. UHC is also firmly envisaged in the WHO constitution of 1948 declaring health a
                            fundamental human
                            right and on the “Health for All” agenda set by the Alma Ata declaration in 1978.</p>
                        <p>Universal Health Coverage has three fundamental component objectives, namely:
                        <ol>
                            <li>Equity in Access to Health Services where everyone who needs healthcare should get them, not
                                only
                                those
                                who can pay for them.</li>
                            <li>The quality of health services should be good enough to improve the health of those
                                receiving
                                services.</li>
                            <li> People should be protected against financial-risk, ensuring that the cost of using the
                                healthcare
                                services does not put people at risk of financial harm.</li>
                            </0l>
                            In Nigeria, the National Health Insurance Scheme (NHIS) was established as the vehicle to
                            achieve
                            Universal Health Coverage in the country. The Scheme was signed into law in 1999 and
                            officially launched
                            on 6th June, 2005. It commenced service delivery to enrollees in September, 2005, and
                            currently has about
                            5% of Nigerians covered under the Scheme in 15 years.
                            In Delta State, the journey towards achieving Universal Health Coverage (UHC) commenced with
                            the
                            transmission of an Executive Bill to the Delta State House of Assembly on the 22nd of June,
                            2015, to
                            establish the Delta State Contributory Health Commission. The Bill went through a first
                            reading on the
                            24th of June, 2015, second reading on the 12th August, 2015, and a public hearing on the
                            26th August,
                            2015. The Bill had its third reading and was passed by the Delta State House of Assembly on
                            the 9th
                            December, 2015. The Bill establishing the Delta State Contributory Health Commission was
                            signed into Law
                            by His Excellency Senator Dr Ifeanyi Okowa, Governor of Delta State, on the 4th of February,
                            2016.</p>
